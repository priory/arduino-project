template<uint16_t MAX_NOTES_IN_TRACK, uint8_t NUM_OF_NOTES>
class MusicPlayer {
public:
    explicit MusicPlayer(uint8_t pin) : _pin(pin) {
        pinMode(this->_pin, OUTPUT);
    }

    void setNotes(const uint16_t (*notes)[NUM_OF_NOTES]) {
        this->_notes = notes;
    }

    void setTrack(const uint8_t (*track)[MAX_NOTES_IN_TRACK][2]) {
        this->_track = track;
    }

    bool isPlaying() {
        return this->_playing;
    }

    uint8_t getBPM() {
        return this->_trackBPM;
    }

    void setBPM(uint8_t bpm) {
        this->_trackBPM = bpm;
        this->resetInterval();
    }

    unsigned int getTrackNotes() {
        return this->_trackNotes;
    }

    void setTrackNotes(unsigned int notes) {
        this->_trackNotes = notes;
    }

    void setupTrack(uint8_t bpm, unsigned int notes) {
        this->setBPM(bpm);
        this->setTrackNotes(notes);
    }

    void resetInterval() {
        this->_interval = 312500 / this->getBPM();
    }

    void loop(unsigned long int &micro) {
        if (this->isPlaying()) {
            if (micro - this->_micro >= this->_interval) {
                if (this->_tick == 0) {
                    this->playNote();
                }
                this->_tick++;
                if (this->_tick >= this->nextNote(1) * 4) {
                    this->killNote();
                    this->_tick = 0;
                    this->_note++;
                    if (this->_note >= this->getTrackNotes()) {
                        this->reset();
                    }
                }
                this->_micro = micro;
            }
        }
    }

    void play() {
        this->_playing = true;
    }

    void pause() {
        this->killNote();
        this->_playing = false;
    }

    void reset() {
        this->resetInterval();
        this->_micro = 0;
        this->_note = 0;
        this->_tick = 0;
    }

private:
    uint8_t _pin;
    bool _playing = false;
    unsigned long int _micro = 0;
    unsigned int _tick = 0;
    unsigned int _interval = 0;
    unsigned int _note = 0;
    const uint16_t (*_notes)[NUM_OF_NOTES];
    const uint8_t (*_track)[MAX_NOTES_IN_TRACK][2];
    uint8_t _trackBPM = 120;
    unsigned int _trackNotes = 0;

    uint8_t getNoteProp(unsigned int note, uint8_t prop = 0) {
        return pgm_read_byte(&this->_track[0][note][prop]);
    }

    uint8_t nextNote(uint8_t prop = 0) {
        return this->getNoteProp(this->_note, prop);
    }

    void killNote() {
        noTone(this->_pin);
    }

    void playNote() {
        unsigned int n = (uint16_t)pgm_read_ptr(&this->_notes[0][this->nextNote()]);
        if (n) {
            tone(this->_pin, n);
        } else {
            killNote();
        }
    }
};

MusicPlayer<256, 52> *mp;

DigitalButton *btnA;
DigitalButton *btnB;

bool toggleBPM = false;

void setup() {
    Serial.begin(9600);

    mp = new MusicPlayer<256, 52>(11);

    mp->setNotes(&NOTES);
    mp->setTrack(&ALL_TRACKS[0]);
    mp->setupTrack(146, 108);

    btnA = new DigitalButton(12);
    btnA->setOnPressed([] {
        if (mp->isPlaying()) mp->pause();
        else mp->play();
    });

    btnB = new DigitalButton(13);
    btnB->setOnPressed([] {
        toggleBPM ^= 1;
        if (toggleBPM) mp->setBPM(73);
        else mp->setBPM(146);
    });
}

unsigned long int counter = 0;
unsigned long int interval = 100;

void loop() {
    unsigned long int micro = micros();

    if (micro - counter >= interval) {

        counter = micro;
    }

    mp->loop(micro);
    btnA->loop(micro);
    btnB->loop(micro);
}
